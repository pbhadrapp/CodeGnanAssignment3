class Person
{
private int age;
private String name;
private float price;
public void setAge(int age) {
	this.age = age;
}
public int getAge() {
	return age;
}
public void setName(String name) {
	this.name = name;
}
public String getName() {
	return name;
}
public void setPrice(float price) {
	this.price = price;
}
public float getPrice() {
	return price;
}

}